//if-else Statement
class conditional {
    public static void main(String[] args) {
        int x = 1;
        
        if (x > 4) {
            System.out.println("x is greater than 4");
        } 
        else {
            System.out.println("x is less than 4");
        }
    }
}


//If-ELse if

class conditional {
    public static void main(String[] args) {
        int x = 4;

        if (x > 4) {
            System.out.println("x is greater than 4");
        }
        else if (x == 4) {
            System.out.println("x is equal to 4");
        } 
        else {
            System.out.println("x is less than 4");
        }
    }
}
